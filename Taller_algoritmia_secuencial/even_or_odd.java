public class even_or_odd {
    public static void main(String[] args) {
    int number=41;
    int even_or_odd=number%2;
    if (even_or_odd==0) {
        System.out.println("The number " + number + " is even.");
    } else {
        System.out.println("The number " + number + " is odd.");
    }
}
}

